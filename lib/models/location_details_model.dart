class LocationDetails {
  final String name;
  final String phone;
  final String addressLine1;
  final String addressLine2;
  final String zip;
  final String city;
  final String state;

  LocationDetails({
    required this.name,
    required this.phone,
    required this.addressLine1,
    required this.addressLine2,
    required this.zip,
    required this.city,
    required this.state,
  });
}